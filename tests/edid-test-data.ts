/**
 * Returns a new array with the concatenated contents of both blocks.
 */
export function createFullEdidArray(
  this: void,
  testData: EdidSample,
): number[] {
  return [...testData.block0, ...(testData.block1 || [])];
}

export function calculateChecksum(this: void, block: number[]): number {
  const sum = block.slice(0, 127).reduce((p, c) => p + c, 0);
  return (256 - (sum % 256)) % 256;
}

/**
 * Returns a NEW deep-cloned copy of {@link testData} with correct checksums.
 */
export function fixChecksums(this: void, testData: EdidSample): EdidSample {
  const fixed = deepmerge<EdidSample>(testData, {});

  fixed.block0[127] = calculateChecksum(fixed.block0);

  // Fix block 1 checksum if it exists
  if (fixed.block1) {
    fixed.block1[127] = calculateChecksum(fixed.block1);
  }

  return fixed;
}

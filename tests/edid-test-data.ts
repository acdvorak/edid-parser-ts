export interface EdidSampleImageSize {
  readonly horizontal: number;
  readonly vertical: number;
}

export interface EdidSampleResult {
  readonly manufacturer: string;
  readonly productCode: number;
  readonly serialNumber?: number;
  readonly edidVersion: '1.0' | '1.1' | '1.2' | '1.3' | '1.4' | '1.5';
  readonly maxImageSize: EdidSampleImageSize;

  /** @example 60 */
  readonly maxRefreshRate?: number;

  readonly hasHDMI14Features?: boolean;
  readonly hasHDMI21Features?: boolean;

  readonly supportsVRR?: boolean;
  readonly supportsALLM?: boolean;
  readonly supportsQMS?: boolean;
  readonly supportsDSC?: boolean;

  readonly maxVRR?: number;
  readonly minVRR?: number;

  readonly supports4K?: boolean;
  readonly supports8K?: boolean;
  readonly supports8K120Hz?: boolean;

  readonly supportsYCbCr420?: boolean;
  readonly supportsBT2020?: boolean;
  readonly supportsDCIP3?: boolean;
  readonly hasWideColorGamut?: boolean;

  readonly supportsHDR?: boolean;
  readonly supportsHDR10?: boolean;
  readonly supportsHLG?: boolean;
  readonly supportsDolbyVision?: boolean;

  /** @example "7.1 surround" */
  readonly roomConfiguration?: string;

  /** @example 600 */
  readonly maxTMDSClock?: number;

  /** @example "4 lanes @ 12 Gbps" */
  readonly fixedRateLink?: string;

  readonly numberOfExtensions?: number;
}

export interface EdidSample {
  readonly description: string;
  readonly block0: number[];
  readonly block1?: number[];
  readonly expectedResults: EdidSampleResult;
}

